class Amplificador {
    public void ligar() {
        System.out.println("Amplificador ligado");
    }

    public void ajustarVolume(int volume) {
        System.out.println("Volume do amplificador ajustado para " + volume);
    }

    public void desligar() {
        System.out.println("Amplificador desligado");
    }
}
